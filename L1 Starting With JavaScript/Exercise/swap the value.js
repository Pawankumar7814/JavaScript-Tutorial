// Declaration and initialization of variables
var a = 5;
var b = 10;

// Printing the value of a and b before swapping the value
console.log("a is " + a);
console.log("b is " + b);


// Swapping the variable's value
var temp = a;
a = b;
b = temp;

// Printing the value of a and after swapping the value
console.log("a is " + a);
console.log("b is " + b);